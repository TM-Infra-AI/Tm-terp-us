<script>
$(function () {
  const $form = $('#contactForm');
  const $name = $('#name'), $email = $('#email'), $phone = $('#phone');
  const $nameErr = $('#nameError'), $emailErr = $('#emailError'), $phoneErr = $('#phoneError');
  const $formMsg = $('#form-message');

  // Regex rules
  const nameRegex = /^[A-Za-z\s]{2,}$/;                // letters & spaces, min 2 chars
  const emailRegex = /^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$/;
  const phoneRegex = /^\d{10}$/;                       // exactly 10 digits

  $name.on('input', function () {
    const sanitized = $(this).val().replace(/[^A-Za-z\s]/g, '');
    if ($(this).val() !== sanitized) $(this).val(sanitized);
    $nameErr.text('');
    $formMsg.text('');
  });

  $phone.on('input', function () {
    let v = $(this).val().replace(/\D/g, '');
    if (v.length > 10) v = v.slice(0, 10);
    $(this).val(v);
    $phoneErr.text('');
    $formMsg.text('');
  });

  $email.on('input', function () {
    $emailErr.text('');
    $formMsg.text('');
  });

  $form.on('submit', function (e) {
    e.preventDefault();
    // clear previous errors
    $nameErr.text(''); $emailErr.text(''); $phoneErr.text(''); $formMsg.text('');

    let valid = true;
    const nameVal = $.trim($name.val());
    const emailVal = $.trim($email.val());
    const phoneVal = $.trim($phone.val());

    if (!nameRegex.test(nameVal)) {
      $nameErr.text('Please enter a valid name.');
      valid = false;
    }
    if (!emailRegex.test(emailVal)) {
      $emailErr.text('Please enter a valid email address.');
      valid = false;
    }
    if (!phoneRegex.test(phoneVal)) {
      $phoneErr.text('Please enter a valid phone number.');
      valid = false;
    }

    if (!valid) {
      if ($nameErr.text()) $name.focus();
      else if ($emailErr.text()) $email.focus();
      else if ($phoneErr.text()) $phone.focus();
      return;
    }

    $.ajax({
      type: 'POST',
      url: $form.attr('action'),
      data: $form.serialize(),
      success: function (resp) {
        $formMsg.html('<div style="color:green;">' + (resp || 'Form submitted successfully.') + '</div>');
        $form[0].reset();
      },
      error: function () {
        $formMsg.html('<div style="color:red;">Something went wrong. Please try again later.</div>');
      }
    });

                // If you prefer normal submission instead of ajax, replace above with: $form.off('submit').submit();
  });
});
</script>



